Diebstahlsicherung	NUMERIC (1)	NOT NULL,
Servolenkung	NUMERIC (1)	NOT NULL,
Elektrische_Fensterheber	NUMERIC (1)	NOT NULL,
Autoradio	NUMERIC (1)	NOT NULL,
Kassette	NUMERIC (1)	NOT NULL,
