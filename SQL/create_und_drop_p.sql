Anzahl_Sitze	NUMERIC (1)	NOT NULL,
Gepaeckstuecke	VARCHAR (25)	NOT NULL,
Mindestalter_zur_Anmietung	NUMERIC (2)	NOT NULL,
Maximales_Alter_zur_Anmietung NUMERIC (2)	NOT NULL,
Fuehrerschein_im_Besitz_seit	NUMERIC (2)	NOT NULL,
Motorleistung	NUMERIC (3)	NOT NULL,
Zylinder	NUMERIC (2)	NOT NULL,
Hubraum	NUMERIC (2,1)	NOT NULL,
Hoechstgeschwindigkeit	NUMERIC (3)	NOT NULL,
Kraftstoffverbrauch	NUMERIC (3,1)	NOT NULL,
Kraftstoffart	VARCHAR (1)	NOT NULL,
Fahrer_Airbag	NUMERIC (1)	NOT NULL,
Beifahrer_Airbag	NUMERIC (1)	NOT NULL,
Seiten_Airbag	NUMERIC (1)	NOT NULL,
Sicherheitsgurte	NUMERIC (1)	NOT NULL,
ABS	NUMERIC (1)	NOT NULL,
