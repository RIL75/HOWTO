CREATE TABLE LAND (
Landkennzeichen	VARCHAR (3)	NOT NULL,
Landesname	VARCHAR (25)	NOT NULL,
CONSTRAINT 	pk_land 	PRIMARY KEY (Landkennzeichen)
);

CREATE TABLE ORT (
Ort_ID	NUMERIC (12)	NOT NULL,
Ortsname	VARCHAR (25)	NOT NULL,
Land	VARCHAR (3)	NOT NULL,
CONSTRAINT 	pk_ort	PRIMARY KEY (Ort_ID),
CONSTRAINT	fk_land	FOREIGN KEY (Land)
REFERENCES	LAND (Landkennzeichen),
CONSTRAINT 	pk_ort_ck	CHECK  (Ort_ID>0)
);

CREATE TABLE PLZ (
PLZ	VARCHAR (10)	NOT NULL,
Ort	NUMERIC (12)	NOT NULL,
CONSTRAINT 	pk_plz	PRIMARY KEY (PLZ),
CONSTRAINT	fk_ort_plz	FOREIGN KEY (Ort)
REFERENCES	ORT (Ort_ID)
);


CREATE TABLE ORTSTEIL (
Ortsteil_ID 	NUMERIC (12)	NOT NULL,
Bezeichnung	VARCHAR (45)	NOT NULL,
Ort	NUMERIC (12)	NOT NULL,
CONSTRAINT 	pk_ortsteil	PRIMARY KEY (Ortsteil_ID),
CONSTRAINT	fk_ort	FOREIGN KEY (Ort)
REFERENCES	ORT (Ort_ID),
CONSTRAINT 	pk_ortsteil_ck	CHECK  (Ortsteil_ID>0)
);

CREATE TABLE STRASSE (
Strasse_ID	NUMERIC (15)	NOT NULL,
Strassename_und_Hausnummer	VARCHAR (45)	NOT NULL,
Laengegrad	NUMERIC (4,2)	NOT NULL,
Breitegrad	NUMERIC (4,2)	NOT NULL,
Ortsteil	NUMERIC (12)	NOT NULL,
CONSTRAINT 	pk_strasse	PRIMARY KEY (Strasse_ID),
CONSTRAINT	fk_ortteil	FOREIGN KEY (Ortsteil)
REFERENCES	ORTSTEIL (Ortsteil_ID),
CONSTRAINT 	pk_strasse_ck	CHECK  (Strasse_ID>0)
);

CREATE TABLE FLUGHAFEN (
Code	VARCHAR (3)	NOT NULL,
Name 	VARCHAR (15)	NOT NULL,
Strasse	NUMERIC	 (15)	NOT NULL,
CONSTRAINT 	pk_flughafen	PRIMARY KEY (Code),
CONSTRAINT	fk_strasse_fg	FOREIGN KEY (Strasse)
REFERENCES	STRASSE (Strasse_ID)
);

CREATE TABLE  KUNDE_1 (
Kunde_ID	NUMERIC	NOT NULL,
Anrede	VARCHAR (4)	NOT NULL,
Titel	VARCHAR (25)	NULL,
Vorname	VARCHAR (25)	NOT NULL,
Name	VARCHAR (25)	NOT NULL,
Firma	VARCHAR (45)	NULL,
Telefon	VARCHAR (25)	NOT NULL,
Mobil_tel	VARCHAR (25)	NOT NULL,
E_mail	VARCHAR (45)	NOT NULL,
Geburtsdatum	DATE	NOT NULL,
Wisard_Nummer	VARCHAR (15)	NOT NULL,
AWD_Nummer	VARCHAR (15)	NULL,
Fuererscheinnummer	VARCHAR (15)	NOT NULL,
Ausstellungsdatum	DATE	NOT NULL,
Kreditkartename	VARCHAR (25)	NOT NULL,
Kreditakartennummer	NUMERIC (15)	NOT NULL,
Gueltigkeitsdatum	DATE	NOT NULL,
Strasse	NUMERIC(15)	NOT NULL,
