CREATE TABLE STATION (
Station_ID	NUMERIC (4)	NOT NULL,
Telefonnummer	VARCHAR (25)	NOT NULL,
Strasse	NUMERIC (15)	NOT NULL,
CONSTRAINT 	pk_station	PRIMARY KEY (Station_ID),
CONSTRAINT	fk_strasse_st	FOREIGN KEY (Strasse)
REFERENCES	STRASSE (Strasse_ID),
CONSTRAINT 	pk_station_ck	CHECK  (Station_ID>0)
);



CREATE TABLE OEFFNUNGZEIT (
Oeffnungzeit_ID	NUMERIC (4)	NOT NULL,
Station	NUMERIC (4)	NOT NULL,
Mo	VARCHAR (15)	NOT NULL,
Di	VARCHAR (15)	NOT NULL,
Mi	VARCHAR (15)	NOT NULL,
Dn	VARCHAR (15)	NOT NULL,
Fr	VARCHAR (15)	NOT NULL,
Sa	VARCHAR (15)	NOT NULL,
So	VARCHAR (15)	NOT NULL,
CONSTRAINT 	pk_of_zeit	PRIMARY KEY (Oeffnungzeit_ID),
CONSTRAINT	fk_station	FOREIGN KEY (Station)
REFERENCES	STATION (Station_ID),
CONSTRAINT 	pk_of_ck	CHECK  (Oeffnungzeit_ID>0)
);


CREATE TABLE AUTOBUCHUNG (
Autobuchung_ID	NUMERIC (15)	NOT NULL,
Abholungszeit	DATE 	NOT NULL,
Rueckgabezeit	DATE 	NOT NULL,
Gesamtpreis	NUMERIC (7,2)	NOT NULL,
Zus_Fahrer	NUMERIC (1)	NOT NULL,
Buchungsdatum	DATE 	NOT NULL,
Abholungsstation	NUMERIC (4)	NOT NULL,
Rueckgabestation	NUMERIC (4)	NOT NULL,
Kunde	NUMERIC (12)	NOT NULL,
Auto	NUMERIC (12)	NOT NULL,
CONSTRAINT 	pk_autobuch	PRIMARY KEY (Autobuchung_ID),
CONSTRAINT	fk_abhol_st	FOREIGN KEY (Abholungsstation)
REFERENCES	STATION (Station_ID),
CONSTRAINT	fk_rueck_st	FOREIGN KEY (Rueckgabestation)
REFERENCES	STATION (Station_ID),
CONSTRAINT	fk_kunde	FOREIGN KEY (Kunde)
REFERENCES	KUNDE_1 (Kunde_ID),
CONSTRAINT	fk_auto	FOREIGN KEY (Auto)
REFERENCES	Auto (Auto_ID),
CONSTRAINT 	pk_autobh_ck	CHECK  (Autobuchung_ID>0),
CONSTRAINT 	zeit_autobh_ck	CHECK  (Rueckgabezeit>Abholungszeit)
);









